// toPascalCase converts a kebab-case, snake_case, or camelCase string to PascalCase.
// "find-by-status" → "FindByStatus", "photoUrls" → "PhotoUrls", "petstore" → "Petstore"
func toPascalCase(s string) string {
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '-' || r == '_'
	})
	var result strings.Builder
	for _, p := range parts {
		if len(p) == 0 {
			continue
		}
		runes := []rune(p)
		result.WriteRune(unicode.ToUpper(runes[0]))
		for _, r := range runes[1:] {
			result.WriteRune(r)
		}
	}
	return result.String()
}

// toCamelCase converts kebab-case or snake_case to camelCase (yargs argv field convention).
// "path-to-req-body" → "pathToReqBody"
func toCamelCase(s string) string {
	if s == "" {
		return s
	}

	// If the name is already a single token (e.g. "firstName"), preserve internal
	// casing and only lowercase the first rune.
	if !strings.ContainsAny(s, "-_") {
		r := []rune(s)
		r[0] = unicode.ToLower(r[0])
		return string(r)
	}

	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '-' || r == '_'
	})
	if len(parts) == 0 {
		return s
	}

	var result strings.Builder
	result.WriteString(strings.ToLower(parts[0]))
	for _, p := range parts[1:] {
		if len(p) == 0 {
			continue
		}
		runes := []rune(p)
		runes[0] = unicode.ToUpper(runes[0])
		result.WriteString(string(runes))
	}
	return result.String()
}

// toGoPackageName converts a command segment to a valid, lowercase Go package name.
// "find-by-status" → "findbystatus", "upload-image" → "uploadimage"
func toGoPackageName(s string) string {
	var result strings.Builder
	for _, r := range strings.ToLower(s) {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			result.WriteRune(r)
		}
	}
	return result.String()
}

// toGoType maps an OpenCLI spec type string to a Go type.
func toGoType(t string, variadic bool) string {
	base := map[string]string{
		"string":  "string",
		"integer": "int64",
		"boolean": "bool",
		"number":  "float64",
	}[t]
	if base == "" {
		base = "string"
	}
	if variadic {
		return "[]" + base
	}
	return base
}

// cobraBindFn returns the cobra Flags() method name for the given spec type and variadic flag.
// Always returns the 'P' variant so that a shorthand alias can be supplied as the third argument.
func cobraBindFn(t string, variadic bool) string {
	if variadic {
		switch t {
		case "integer":
			return "Int64SliceVarP"
		case "boolean":
			return "BoolSliceVarP"
		case "number":
			return "Float64SliceVarP"
		default:
			return "StringArrayVarP"
		}
	}
	switch t {
	case "integer":
		return "Int64VarP"
	case "boolean":
		return "BoolVarP"
	case "number":
		return "Float64VarP"
	default:
		return "StringVarP"
	}
}

// cobraDefaultVal returns the Go literal for the zero/empty default value of a cobra flag.
func cobraDefaultVal(t string, variadic bool) string {
	if variadic {
		switch t {
		case "integer":
			return "[]int64{}"
		case "boolean":
			return "[]bool{}"
		case "number":
			return "[]float64{}"
		default:
			return "[]string{}"
		}
	}
	switch t {
	case "integer":
		return "0"
	case "boolean":
		return "false"
	case "number":
		return "0"
	default:
		return `""`
	}
}

// buildMethodName constructs the ActionsInterface method name for a command.
// e.g. binaryPascal="Petstore", segments=["pet","add"] → "PetstorePetAdd"
func buildMethodName(segments []string) string {
	parts := make([]string, 0, len(segments)+1)
	for _, seg := range segments {
		parts = append(parts, toPascalCase(seg))
	}
	return strings.Join(parts, "")
}

// commandFuncName returns the NewCmd* function name for the given segment path.
// [] → "NewCmdRoot", ["pet"] → "NewCmdPet", ["pet","add"] → "NewCmdPetAdd"
func commandFuncName(segments []string) string {
	if len(segments) == 0 {
		return "NewCmdRoot"
	}
	parts := make([]string, len(segments))
	for i, s := range segments {
		parts[i] = toPascalCase(s)
	}
	return "NewCmd" + strings.Join(parts, "")
}

// getSpecFuncName returns the getSpec*Cmd function name for the given segment path.
// [] → "getSpecRootCmd", ["pet","add"] → "getSpecPetAddCmd"
func getSpecFuncName(segments []string) string {
	if len(segments) == 0 {
		return "getSpecRootCmd"
	}
	parts := make([]string, len(segments))
	for i, s := range segments {
		parts[i] = toPascalCase(s)
	}
	return "getSpec" + strings.Join(parts, "") + "Cmd"
}

// commandOutPath returns the output file path (under gencli/) for the given segment path.
// [] → "gencli/root.go", ["pet"] → "gencli/pet.go", ["pet","add"] → "gencli/pet_add.go"
func commandOutPath(segments []string) string {
	if len(segments) == 0 {
		return "gencli/cmd_root.gen.go"
	}
	parts := make([]string, len(segments))
	for i, s := range segments {
		parts[i] = toGoPackageName(s)
	}
	return "gencli/cmd_" + strings.Join(parts, "_") + ".gen.go"
}
