// toTSType maps an OpenCLI spec type to a TypeScript type string.
func toTSType(t string, variadic bool) string {
	base := map[string]string{
		"string":  "string",
		"integer": "number",
		"boolean": "boolean",
		"number":  "number",
	}[t]
	if base == "" {
		base = "string"
	}
	if variadic {
		return base + "[]"
	}
	return base
}

// yargsDefaultVal returns a TypeScript literal default value for a flag.
func yargsDefaultVal(t string, variadic bool) string {
	if variadic {
		return ""
	}
	switch t {
	case "integer", "number":
		return "0"
	case "boolean":
		return "false"
	default:
		return ""
	}
}

