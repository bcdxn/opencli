// Docs generates documentation based on the given OpenCLI document
func Docs(doc *spec.Document, options ...GenDocsOption) ([]byte, error) {
	opts := &genDocsOptions{
		Format:        Markdown,
		HTMLFlavor:    StandalonePage,
		IncludeBadge:  true,
		IncludeFooter: true,
	}

	for _, option := range options {
		option(opts)
	}

	if !opts.Format.IsValid() {
		return []byte{}, errors.New("invalid documentation format")
	}

	if opts.Format == HTML && !opts.HTMLFlavor.IsValid() {
		return []byte{}, errors.New("invalid HTML flavor")
	}

	if opts.Format == ManPage {
		return []byte{}, fmt.Errorf("%s format support is not yet implemented", opts.Format)
	}

	return genDocs(docsTmplData{*opts, doc})
}

/* Functional Options
------------------------------------------------------------------------------------------------- */

// GenDocsOption is a functional option to configure the GenDocs function
type GenDocsOption func(*genDocsOptions)

// DocsWithoutFooter option will remove the `generated by OpenCLI` footer appended to the documents by default.
func DocsWithoutFooter() GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.IncludeFooter = false
	}
}

// DocsWithoutBadge option will remove shields.io badge that indicates compliance with the OpenCLI Spec prepended to markdown documents by default.
func DocsWithoutBadge() GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.IncludeBadge = false
	}
}

// DocsWithFormat sets the desired output format of the documentation.
func DocsWithFormat(format DocFormat) GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.Format = format
	}
}

// DocsWithHTMLFlavor sets the HTML output flavor when using the HTML format.
// Use EmbeddableComponent for a self-contained <div> that can be dropped into an existing
// React, Svelte, Angular, or vanilla-JS SPA. Use StandalonePage (the default) for a
// complete HTML document that can be opened directly in a browser.
func DocsWithHTMLFlavor(flavor HTMLFlavor) GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.HTMLFlavor = flavor
	}
}

// DocFormat is the output format for generated documentation.
type DocFormat string

const (
	Markdown DocFormat = "MARKDOWN"
	HTML     DocFormat = "HTML"
	ManPage  DocFormat = "MAN"
)

func (f DocFormat) IsValid() bool {
	switch f {
	case Markdown, HTML, ManPage:
		return true
	}
	return false
}

// HTMLFlavor controls which HTML output variant is generated.
type HTMLFlavor string

const (
	// StandalonePage produces a complete <!doctype html> document ready to open in a browser.
	StandalonePage HTMLFlavor = "PAGE"
	// EmbeddableComponent produces a self-contained <style>+<div>+<script> fragment that
	// can be injected into any existing web page or SPA without a build step.
	EmbeddableComponent HTMLFlavor = "COMPONENT"
)

func (f HTMLFlavor) IsValid() bool {
	switch f {
	case StandalonePage, EmbeddableComponent:
		return true
	}
	return false
}

/* Private functions and types
------------------------------------------------------------------------------------------------- */

// genDocsOptions represents the configurable options when generating documentation.
// The options are meant to be configured using the functional options pattern.
type genDocsOptions struct {
	Format        DocFormat
	HTMLFlavor    HTMLFlavor
	IncludeBadge  bool
	IncludeFooter bool
}

// docsTmplData represents the data used to render the documentation template.
type docsTmplData struct {
	Opts genDocsOptions
	Doc  *spec.Document
}

// genDocs is a helper/orchestrator function that calls the format-appropriate genDocs function
func genDocs(data docsTmplData) ([]byte, error) {
	switch data.Opts.Format {
	case Markdown:
		return genDocsMarkdown(data)
	case HTML:
		return genDocsHTML(data)
	}

	return []byte{}, fmt.Errorf("unsupported documentation format: %s", data.Opts.Format)
}

//go:embed templates/md/markdown.tmpl
var mdTemplate []byte

// genDocsMarkdown executes the template in combination with the spec data + options.
// The result is a fully rendered markdown document.
func genDocsMarkdown(data docsTmplData) ([]byte, error) {
	t, err := template.New("markdown.tmpl").Parse(string(mdTemplate))
	if err != nil {
		return []byte{}, fmt.Errorf("unable to generate markdown docs template: %w", err)
	}

	buf := bytes.NewBuffer([]byte{})

	err = t.ExecuteTemplate(buf, "markdown.tmpl", data)
	if err != nil {
		return []byte{}, fmt.Errorf("unable to render markdown docs: %w", err)
	}

	return buf.Bytes(), nil
}

//go:embed templates/html
var htmlTemplateFiles embed.FS

// htmlTemplateFuncs returns the template.FuncMap used by all HTML templates.
func htmlTemplateFuncs() htmltemplate.FuncMap {
	return htmltemplate.FuncMap{
		"slugify":  slugify,
		"mdToHTML": mdToHTML,
	}
}

// genDocsHTML renders the HTML documentation using the appropriate flavor template.
func genDocsHTML(data docsTmplData) ([]byte, error) {
	t, err := htmltemplate.New("ocli-html").
		Funcs(htmlTemplateFuncs()).
		ParseFS(htmlTemplateFiles, "templates/html/*.tmpl")
	if err != nil {
		return []byte{}, fmt.Errorf("unable to parse HTML docs templates: %w", err)
	}

	var rootTmpl string
	switch data.Opts.HTMLFlavor {
	case EmbeddableComponent:
		rootTmpl = "component.tmpl"
	default:
		rootTmpl = "page.tmpl"
	}

	buf := bytes.NewBuffer([]byte{})
	err = t.ExecuteTemplate(buf, rootTmpl, data)
	if err != nil {
		return []byte{}, fmt.Errorf("unable to render HTML docs: %w", err)
	}

	return buf.Bytes(), nil
}

// slugify converts a command line string into an anchor-safe HTML id.
// e.g. "petstore pets add" -> "petstore-pets-add"
var slugifyRe = regexp.MustCompile(`[^a-z0-9]+`)

func slugify(s string) string {
	s = strings.ToLower(s)
	s = slugifyRe.ReplaceAllString(s, "-")
	s = strings.Trim(s, "-")
	return s
}

// mdToHTML converts a Markdown string to safe HTML using goldmark.
// The result is typed as htmltemplate.HTML so html/template will not double-escape it.
var mdParser = goldmark.New(
	goldmark.WithExtensions(extension.GFM),
	goldmark.WithRendererOptions(
		html.WithUnsafe(), // allow raw HTML in descriptions if present
	),
)

func mdToHTML(s string) htmltemplate.HTML {
	if s == "" {
		return ""
	}
	var buf bytes.Buffer
	if err := mdParser.Convert([]byte(s), &buf); err != nil {
		// Fallback: return escaped plain text
		return htmltemplate.HTML(htmltemplate.HTMLEscapeString(s))
	}
	return htmltemplate.HTML(buf.String())
}
