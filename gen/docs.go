package gen

import (
	"bytes"
	_ "embed"
	"errors"
	"fmt"
	"text/template"

	"github.com/bcdxn/opencli/spec"
)

// Docs generates documentation based on the given OpenCLI document
func Docs(doc *spec.Document, options ...GenDocsOption) ([]byte, error) {
	opts := &genDocsOptions{
		Format:        Markdown,
		IncludeBadge:  true,
		IncludeFooter: true,
	}

	for _, option := range options {
		option(opts)
	}

	if !opts.Format.IsValid() {
		return []byte{}, errors.New("invalid documentation format")
	}

	// currently only supports markdown
	if opts.Format != Markdown {
		return []byte{}, fmt.Errorf("%s format support is not yet implemented", opts.Format)
	}

	return genDocs(docsTmplData{*opts, doc})
}

/* Functional Options
------------------------------------------------------------------------------------------------- */

// GenDocsOption is a functional option to configure the GenDocs function
type GenDocsOption func(*genDocsOptions)

// WithoutFooter option will remove the `generated by OpenCLI` footer appended to the documents by default.
func DocsWithoutFooter() GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.IncludeFooter = false
	}
}

// WithoutBadge option will remove shields.io badget that indicates compliance with the OpenCLI Spec prepended to markdown documents by default.
func DocsWithoutBadge() GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.IncludeBadge = false
	}
}

// Set the desired format of the documentation.
func DocsWithFormat(format Format) GenDocsOption {
	return func(opts *genDocsOptions) {
		opts.Format = format
	}
}

type Format string

const (
	Markdown Format = "MARKDOWN"
	HTML     Format = "HTML"
	ManPage  Format = "MAN"
)

func (f Format) IsValid() bool {
	switch f {
	case Markdown, HTML, ManPage:
		return true
	}
	return false
}

/* Private functions and types
------------------------------------------------------------------------------------------------- */

// genDocsOptions represents the configurable options when generating documentation.
// The options are meant to be configured using the functional options pattern.
type genDocsOptions struct {
	Format        Format
	IncludeBadge  bool
	IncludeFooter bool
}

// docsTmplData represents the data used to render the documentation template.
type docsTmplData struct {
	Opts genDocsOptions
	Doc  *spec.Document
}

// genDocs is a helper/orchestrator function that calls the format-appropriate genDocs function
func genDocs(data docsTmplData) ([]byte, error) {
	switch data.Opts.Format {
	case Markdown:
		return genDocsMarkdown(data)
		// TODO: support html, man
	}

	return []byte{}, fmt.Errorf("unsupported documentation format: %s", data.Opts.Format)
}

//go:embed templates/docs/markdown.tmpl
var mdTemplate []byte

// genDocsMarkdown executes the template in combination with the spec data + options.
// // The result is a fully rendered markdown document.
func genDocsMarkdown(data docsTmplData) ([]byte, error) {
	t, err := template.New("markdown.tmpl").Parse(string(mdTemplate))
	if err != nil {
		return []byte{}, fmt.Errorf("unable to generate markdown docs template: %w", err)
	}

	buf := bytes.NewBuffer([]byte{})

	err = t.ExecuteTemplate(buf, "markdown.tmpl", data)
	if err != nil {
		return []byte{}, fmt.Errorf("unable to render markdown docs: %w", err)
	}

	return buf.Bytes(), nil
}
